window.consultoriaSortKey = function (c) {
  var periodo = (c && (c.periodo || c.periodoEn)) || '';
  if (!periodo || periodo === '—') return 0;

  var lower = String(periodo).toLowerCase();
  var monthMap = {
    janeiro: 1,
    february: 2,
    fevereiro: 2,
    march: 3,
    marco: 3,
    'março': 3,
    april: 4,
    abril: 4,
    may: 5,
    maio: 5,
    june: 6,
    junho: 6,
    july: 7,
    julho: 7,
    august: 8,
    agosto: 8,
    september: 9,
    setembro: 9,
    october: 10,
    outubro: 10,
    november: 11,
    novembro: 11,
    december: 12,
    dezembro: 12
  };

  var maxYear = 0;
  var years = lower.match(/(19|20)\d{2}/g) || [];
  years.forEach(function (y) {
    var n = parseInt(y, 10);
    if (n > maxYear) maxYear = n;
  });
  if (!maxYear) return 0;

  var month = 1;
  Object.keys(monthMap).forEach(function (name) {
    var idx = lower.lastIndexOf(name);
    if (idx !== -1 && monthMap[name] > month) month = monthMap[name];
  });

  return maxYear * 100 + month;
};

window.getConsultoriasSortedWithIndex = function (list) {
  var source = Array.isArray(list) ? list : window.CONSULTORIAS_FICHAS || [];
  return source
    .map(function (item, index) {
      return {
        index: index,
        item: item,
        sortKey: window.consultoriaSortKey(item)
      };
    })
    .sort(function (a, b) {
      if (b.sortKey !== a.sortKey) return b.sortKey - a.sortKey;
      var aTitle = (a.item && (a.item.titulo || a.item.tituloEn)) || '';
      var bTitle = (b.item && (b.item.titulo || b.item.tituloEn)) || '';
      return aTitle.localeCompare(bTitle);
    });
};

window.getConsultoriasPublicoDestaque = function (list) {
  var sorted = window.getConsultoriasSortedWithIndex(list);
  return sorted.filter(function (entry) {
    return !!(entry.item && entry.item.publicoDestaque);
  });
};

window.getConsultoriasNaoDestaque = function (list) {
  var sorted = window.getConsultoriasSortedWithIndex(list);
  return sorted.filter(function (entry) {
    return !(entry.item && entry.item.publicoDestaque);
  });
};

/**
 * Campos localizados para cartões e modal (fallback PT se *En ausente).
 * UI pública usa contexto / atividades / resultados (divulgacao); problema/estrategia/entregaveis mantêm-se nos dados para arquivo interno.
 */
window.consultoriaLocalized = function (c, lang) {
  var en = lang === 'en';
  var nomePublico = c.clienteNomePublico !== false;
  var clienteLinha;
  var clienteNotaSobPedido = false;
  if (nomePublico) {
    clienteLinha = en && c.clienteEn ? c.clienteEn : c.cliente;
  } else {
    var res = c.clienteResumo || {};
    clienteLinha = en ? res.en || res.pt || '' : res.pt || res.en || '';
    if (!clienteLinha) {
      clienteLinha = en
        ? 'National and international institutional partners'
        : 'Parceiros institucionais nacionais e internacionais';
    }
    clienteNotaSobPedido = true;
  }

  var div = c.divulgacao;
  var contexto;
  var atividades;
  var resultadosLista;
  if (div) {
    contexto = en ? div.contexto.en || div.contexto.pt : div.contexto.pt || div.contexto.en;
    atividades = en
      ? div.atividades.en || div.atividades.pt || []
      : div.atividades.pt || div.atividades.en || [];
    resultadosLista = en
      ? div.resultados.en || div.resultados.pt || []
      : div.resultados.pt || div.resultados.en || [];
  } else {
    contexto = en && c.problemaEn ? c.problemaEn : c.problema;
    atividades =
      en && c.estrategiaEn && c.estrategiaEn.length ? c.estrategiaEn : c.estrategia || [];
    resultadosLista =
      en && c.entregaveisEn && c.entregaveisEn.length ? c.entregaveisEn : c.entregaveis || [];
  }

  return {
    titulo: en && c.tituloEn ? c.tituloEn : c.titulo,
    periodo: en && c.periodoEn ? c.periodoEn : c.periodo,
    cliente: clienteLinha,
    clienteNomePublico: nomePublico,
    clienteNotaSobPedido: clienteNotaSobPedido,
    contexto: contexto,
    atividades: atividades,
    resultados: resultadosLista,
    problema: en && c.problemaEn ? c.problemaEn : c.problema,
    estrategia:
      en && c.estrategiaEn && c.estrategiaEn.length ? c.estrategiaEn : c.estrategia || [],
    entregaveis:
      en && c.entregaveisEn && c.entregaveisEn.length ? c.entregaveisEn : c.entregaveis || []
  };
};
